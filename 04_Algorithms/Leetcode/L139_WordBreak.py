class Solution:
    def wordBreak(self, s, wordDict):
        res = []
        if s == '':
            return True
        while res == []:
            mask = [True] * len(wordDict)
            for i, word in enumerate(wordDict):
                if s.startswith(word):
                    result = self.wordBreak(s[len(word):],wordDict)
                    if result == False:
                        mask[i]=False
                        continue
                    mask = [True] * len(wordDict)
                    res.append(result)
                else:
                    mask[i] = False
            if mask == [False] * len(wordDict):
                return False
        return any(res)

